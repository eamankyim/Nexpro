const escapeHtml = (value) =>
  value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');

const formatInline = (value) => {
  const escaped = escapeHtml(value);
  return escaped.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
};

const renderLine = (line, index) => {
  const trimmed = line.trim();
  if (!trimmed) return null;

  if (trimmed.startsWith('# ')) {
    return (
      <h1 key={index} className="text-3xl font-bold tracking-tight text-foreground md:text-4xl">
        {trimmed.replace(/^#\s+/, '')}
      </h1>
    );
  }

  if (trimmed.startsWith('## ')) {
    return (
      <h2 key={index} className="pt-6 text-xl font-semibold text-foreground md:text-2xl">
        {trimmed.replace(/^##\s+/, '')}
      </h2>
    );
  }

  if (trimmed.startsWith('### ')) {
    return (
      <h3 key={index} className="pt-3 text-lg font-semibold text-foreground">
        {trimmed.replace(/^###\s+/, '')}
      </h3>
    );
  }

  if (/^\d+\.\s+/.test(trimmed)) {
    return (
      <p
        key={index}
        className="pl-4 text-sm leading-7 text-muted-foreground md:text-base"
        dangerouslySetInnerHTML={{ __html: formatInline(trimmed) }}
      />
    );
  }

  if (trimmed.startsWith('> ')) {
    return (
      <blockquote
        key={index}
        className="border-l-4 border-primary bg-muted/50 p-4 text-sm leading-7 text-foreground md:text-base"
        dangerouslySetInnerHTML={{ __html: formatInline(trimmed.replace(/^>\s+/, '')) }}
      />
    );
  }

  return (
    <p
      key={index}
      className="text-sm leading-7 text-muted-foreground md:text-base"
      dangerouslySetInnerHTML={{ __html: formatInline(trimmed.replace(/ {2}$/g, '')) }}
    />
  );
};

